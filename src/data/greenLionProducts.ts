// Helper function to get Green Lion products by category
export const getGreenLionProductsByCategory = (category: string): GreenLionProduct[] => {
  return greenLionProducts.filter(
    (product) =>
      product.category === category ||
      product.secondaryCategories?.includes(category)
  );
};

// Helper function to get Green Lion product by ID
export const getGreenLionProductById = (id: number): GreenLionProduct | undefined => {
  return greenLionProducts.find((product) => product.id === id);
};

// Get all unique brands (for filtering)
export const getAllBrands = (): string[] => {
  const brands = new Set(greenLionProducts.map((product) => product.brand));
  return Array.from(brands);
};

